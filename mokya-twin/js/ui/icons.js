/**
 * icons.js — 24×24 1bpp menu icons
 *
 * Each icon is hand-drawn as ASCII art (`#` = pixel on, `.` = pixel off),
 * 24 chars wide × 24 rows tall. `bitmapFromAscii()` packs the rows MSB-first
 * into a Uint8Array of 24 × 3 = 72 bytes.
 *
 * Style: pure black-and-white blocks to match the firmware Unifont glyphs.
 */

const W = 24;
const H = 24;

function bitmapFromAscii(rows) {
  if (rows.length !== H) throw new Error(`icon must be ${H} rows, got ${rows.length}`);
  const rowBytes = (W + 7) >> 3;
  const out = new Uint8Array(H * rowBytes);
  for (let y = 0; y < H; y++) {
    const r = rows[y];
    if (r.length !== W) throw new Error(`row ${y} must be ${W} cols, got ${r.length}: "${r}"`);
    for (let x = 0; x < W; x++) {
      if (r[x] === '#') out[y * rowBytes + (x >> 3)] |= 0x80 >> (x & 7);
    }
  }
  return out;
}

